package com.mygdx.game.controller;

public class HitBox {
    private float x;
    private float y;
    private int width;
    private int height;

    public HitBox(float x, float y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
